public void getAllItems() {
        try {
            ItemController itemController = new ItemControllerImpl();

            List<ItemDTO> lis = itemController.getAllItems();
            for (ItemDTO li : lis) {
                cmbDescription.addItem(li.getDescription());
            }
        } catch (Exception ex) {
            Logger.getLogger(PlaceOrderForm.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    private void loadSystemDate() {
        Date d1 = new Date(); //Current System date
        SimpleDateFormat df = new SimpleDateFormat("YYYY-MM-dd");
        String formatDate = df.format(d1);
        txtOrderDate.setText(formatDate);

    }

    private void setTotle() {
        DefaultTableModel dtm = (DefaultTableModel) tblOrder.getModel();
        double total = 0;
        for (int i = 0; i < dtm.getRowCount(); i++) {
            total += Double.parseDouble(dtm.getValueAt(i, 3).toString());
        }
        txtTotalAmount.setText(Double.toString(total));
    }
