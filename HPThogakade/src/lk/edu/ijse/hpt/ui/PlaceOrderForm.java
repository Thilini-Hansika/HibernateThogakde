    private void txtQtyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtQtyActionPerformed
        int x = Integer.parseInt(txtQty.getText());
        int qtyOnHAnd = Integer.parseInt(txtQtyOnHand.getText()) - x;
        if (qtyOnHAnd >= 0) {
            txtQtyOnHand.setText(Integer.toString(qtyOnHAnd));
            Double amount = x * Double.parseDouble(txtUnitPrice.getText());
            if (cmbDescription.getSelectedIndex() == -1) {
                JOptionPane.showMessageDialog(this, "Item Not Selected");
            } else {
                DefaultTableModel dtm = (DefaultTableModel) tblOrder.getModel();

                Object[] rowdata = {cmbDescription.getSelectedItem(), txtUnitPrice.getText(), x, amount};
                dtm.addRow(rowdata);
                setTotle();
            }
        }

    }//GEN-LAST:event_txtQtyActionPerformed

